    public void nuevoAlumno(Alumno alumno){
        
        logica.getAlumnos().add(alumno);
        
        this.jTable1.updateUI();
        
    }
    
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        int filaSeleccionada = jTable1.getSelectedRow();

        if (filaSeleccionada >= 0 && filaSeleccionada < logica.getAlumnos().size()) {

            logica.getAlumnos().remove(filaSeleccionada);

            this.jTable1.updateUI();
            
            JOptionPane.showMessageDialog(this, "Fila Borrada", "Borrado", JOptionPane.INFORMATION_MESSAGE);

        }
        else{
            
            JOptionPane.showMessageDialog(this, "No hay fila seleccionada", "Error", JOptionPane.WARNING_MESSAGE);
            
        }

    }//GEN-LAST:event_jButton3ActionPerformed

